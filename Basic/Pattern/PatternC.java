class PatternC {
	public static void main(String[] args) {
		for(int i=0; i<=6;i++) {
			for(int j=0; j<=7; j++) {
				if(j==0||i==0||j==1||i==1|i==6|| i==5) {
					System.out.print("C");
				}else{
					System.out.print(" ");
				}
			}
			System.out.println();
		}
	}
}

