class DataType{
	public static void main(String[] args) {
		byte month = 4;
		short year = 1998;
		int pin = 818109;
		long phone = 7903508584l;
		float pi = 3.14f;
		double tax = 3.16666666;
		char grade = 'A';
		boolean pass = true;

		System.out.println("byte month: "+month);
		System.out.println("short year: "+year);
		System.out.println("int month: "+pin);
		System.out.println("long phone: "+phone);
		System.out.println("float pi: "+pi);
		System.out.println("double tax: "+tax);
		System.out.println("char grade: "+grade);
		System.out.println("boolean pass : "+pass);
	}
}